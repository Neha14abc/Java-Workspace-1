package demoForLOOP;
public class demoTable5and10 {
    public static void main(String[] args) {
        int five = 5;
        for (int i = 1; i <= 10; i++) {
            int multiply = five * i;
            System.out.println(five + " * " + i + " = " + multiply);
        }
        System.out.println("\n");
        int ten = 10;
        for (int i = 1; i <= 10; i++) {
            int multiply = ten * i;
            System.out.println(ten + " * " + i + " = " + multiply);
        }
    }
}