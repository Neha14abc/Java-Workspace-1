package Demo_Debugging;

public class Break_Debugging {
    public static void main(String[] args) {
        int day =2;
        switch (day) {
            case 1:
                System.out.println("First day of a Week: Monday");
                break;
            case 2:
                System.out.println("Second day of a Week: Tuesday");
                break;
        }
    }
}

