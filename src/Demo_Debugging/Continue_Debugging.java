package Demo_Debugging;

public class Continue_Debugging {
        public static void main(String[] args) {
            int number = 2375;
            if (number % 2 == 0) {
                System.out.println("They are EVEN ..." + number);
            }
            if (number % 2 != 0) {
                System.out.println("They are ODD ..." + number);
            }
        }
}
