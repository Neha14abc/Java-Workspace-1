package demoConditions;

public class evenOdd {
    public static void main(String[] args) {
        int number = 2376;
        if (number % 2 == 0) {
            System.out.println("They are EVEN ..." + number);
        }
        if (number % 2 != 0) {
            System.out.println("They are ODD ..." + number);
        }
        int number1 = 5697;
        if (number1 % 2 == 0) {
            System.out.println("They are EVEN ..." + number1);
        }
        if (number1 % 2 != 0) {
            System.out.println("They are ODD ..." + number1);
        }

    }
}
